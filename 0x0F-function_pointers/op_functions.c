/**
 * op_add - adds 2 integers
 * @a: first integer
 * @b: second integer
 * Return: integer of addition of 2 numbers
 */
int op_add(int a, int b)
{
return (a + b);
}
/**
 * op_sub - substractes 2 integers
 * @a: first integer
 * @b: second integer
 * Return: integer of substraction of 2 numbers
 */
int op_sub(int a, int b)
{
return (a - b);
}
/**
 * op_mul - multiplies 2 integers
 * @a: first integer
 * @b: second integer
 * Return: integer of multoplication of 2 numbers
 */
int op_mul(int a, int b)
{
return (a * b);
}
/**
 * op_div - divides 2 integers
 * @a: first integer
 * @b: second integer
 * Return: integer of division of 2 numbers
 */
int op_div(int a, int b)
{
return (a / b);
}
int op_mod(int a, int b)
{
return (a % b);
}
